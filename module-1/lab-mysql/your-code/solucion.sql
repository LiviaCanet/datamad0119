CREATE TABLE customers (customerID INT, name VARCHAR(45), PhoneNumber INT, Email VARCHAR(45), Address VARCHAR(200), city VARCHAR(45), StateProvince VARCHAR(45), Country VARCHAR(45), ZIP INT); 
CREATE TABLE salesperson (StaffID INT, Name VARCHAR(45), Store VARCHAR(45));
CREATE TABLE invoices (IDinvoices INT, Date DATETIME, Car VARCHAR(45), Customer VARCHAR(45), SalesPerson VARCHAR(45));


 